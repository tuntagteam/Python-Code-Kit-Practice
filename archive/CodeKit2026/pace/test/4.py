# # 4. ระบบเก็บเหรียญของนักผจญภัย

# นักผจญภัยเดินผ่านดันเจียนที่มีเหรียญหลายช่อง

# แต่ละช่องมีจำนวนเหรียญแตกต่างกัน

# นักผจญภัยต้องการเลือก “ช่องที่มีเหรียญมากที่สุด”

# ## Input
# จำนวนช่อง n

# จำนวนเหรียญในแต่ละช่อง

# ## Output
# จำนวนเหรียญสูงสุด

# ## Example

# ### Input
# 6
# 4 9 2 11 7 1

# ### Output
# 11

# ---
x=int(input())
y=list(map(int, input().split()))
xy=max(y)

print(xy)
